switch (friend) {
    case "Autumn":
        console.log("Hey Autumn, when are we going rock wall climbing?");
        break;
    case "Danielle":
        console.log("Hey Danielle, how is Finn?");
        break;
    case "Josh":
        console.log("Hey Josh, let's go to Target!");
        break;
    default:
        console.log(`I'm sorry, ${friend}, but do I know you?`);
}



