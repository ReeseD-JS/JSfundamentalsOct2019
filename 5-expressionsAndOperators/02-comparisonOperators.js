// Comparison Operators

//Equal (checks to see if they are equal)

'3' == 3;     //The checks only to see if values are equal

'3' === 3;   //This checks to see if value match AND data types match (false because one data type is a string and the other is a number)

//Not Equal

3 != 4; //3 !(does not) equal 4

//Strictly not equal

'3' !== 3;

// Greater Than

3 > 2;

// Less Than

2 < 3;

// Greater Than or Equal to

3 >= 2;

//Lesser Than or Equal To 

2 <= 3;

// And (to check if something is both)

2 && 3;

// Or

2 || 3;